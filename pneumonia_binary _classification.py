"""# Grad-CAM Implementation"""

def find_last_conv_layer(model):
    # Find the last convolutional layer in the base model
    conv_layers = [layer for layer in model.layers if isinstance(layer, tf.keras.layers.Conv2D)]
    return conv_layers[-1].name

def make_gradcam_heatmap(img_tensor, model):
    # 1. Extract the base EfficientNet model
    base_model = model.layers[1]

    # 2. Extract the classifier layers (GlobalAveragePooling, Dropout, Dense)
    classifier_layers = model.layers[2:]

    with tf.GradientTape() as tape:
        # 3. Pass the image through the base model to get the feature maps
        # (training=False ensures BatchNorm and Dropout behave correctly)
        last_conv_layer_output = base_model(img_tensor, training=False)

        # Tell the gradient tape to track our feature maps
        tape.watch(last_conv_layer_output)

        # 4. Pass the feature maps through the rest of the classifier
        preds = last_conv_layer_output
        for layer in classifier_layers:
            preds = layer(preds, training=False)

        # Since you are doing binary classification with 1 output node (sigmoid),
        # the prediction score is just the 0th index.
        class_channel = preds[0, 0]

    # Calculate gradients of the class score with respect to the feature map
    grads = tape.gradient(class_channel, last_conv_layer_output)

    # Average gradients spatially
    pooled_grads = tf.reduce_mean(grads, axis=(0, 1, 2))

    # Multiply feature maps by importance weights
    last_conv_layer_output = last_conv_layer_output[0]
    heatmap = last_conv_layer_output @ pooled_grads[..., tf.newaxis]
    heatmap = tf.squeeze(heatmap)

    # Apply ReLU: Keep only features that have a positive influence
    heatmap = tf.maximum(heatmap, 0)

    # Normalize between 0 and 1
    max_val = tf.math.reduce_max(heatmap)
    if max_val == 0:
        max_val = 1e-8
    heatmap = heatmap / max_val

    return heatmap.numpy()

def display_gradcam(img_path, heatmap, alpha=0.4):
    # 1. Read the original image in BGR format
    img = cv2.imread(img_path)
    img = cv2.resize(img, (224, 224))

    img = np.uint8(img)

    # 2. Resize heatmap to match the image dimensions
    heatmap_resized = cv2.resize(heatmap, (img.shape[1], img.shape[0]))

    # 3. Rescale heatmap to 0-255 range and convert to uint8
    heatmap_rescaled = np.uint8(255 * heatmap_resized)

    # 4. Apply Jet colormap (OpenCV returns BGR by default)
    jet = cv2.applyColorMap(heatmap_rescaled, cv2.COLORMAP_JET)

    # 5. Superimpose the heatmap on the original image
    superimposed_img = cv2.addWeighted(img, 1 - alpha, jet, alpha, 0)

    # 6. Plot the results (Convert BGR to RGB for Matplotlib)
    plt.figure(figsize=(10, 5))
    plt.subplot(1, 2, 1)
    plt.title('Original Image')
    plt.imshow(cv2.cvtColor(img, cv2.COLOR_BGR2RGB))
    plt.axis('off')

    plt.subplot(1, 2, 2)
    plt.title('Grad-CAM Heatmap')
    plt.imshow(cv2.cvtColor(superimposed_img, cv2.COLOR_BGR2RGB))
    plt.axis('off')

    plt.tight_layout()
    plt.show()

