"""# Single Image Inference"""

def predict_and_explain_xray(img_path, model, threshold=0.50):
    print(f"\nProcessing X-Ray: {os.path.basename(img_path)}")
    true_diagnosis = "Pneumonia" if "PNEUMONIA" in img_path.upper() else "Normal"

    # 1. Preprocess the image
    img_tensor, _ = parse_image(img_path, label=0) # Dummy label for the parser
    img_tensor_batch = tf.expand_dims(img_tensor, axis=0) # Add batch dimension for the model
    # 2. Make Prediction
    prob = model.predict(img_tensor_batch, verbose=0)[0][0]
    # 3. Apply Clinical Logic
    prediction = "Pneumonia" if prob >= threshold else "Normal"
    confidence = prob if prediction == "Pneumonia" else (1 - prob)
    is_correct = (prediction == true_diagnosis)
    status_icon = "✅ MATCH" if is_correct else "❌ MISMATCH"

    print("-" * 35)
    print(f"Ground Truth   : {true_diagnosis}")
    print(f"AI Diagnosis   : {prediction} ({status_icon})")
    print(f"Confidence     : {confidence:.2%}")
    print(f"Raw Prob       : {prob:.4f}")
    print("-" * 35)
    # 4. Generate and display Grad-CAM
    heatmap = make_gradcam_heatmap(img_tensor_batch, model)
    display_gradcam(img_path, heatmap)

